/* 定长队列中取最大值,但leetcode超时 */
export default function maxSlidingWindow(nums, k) {
  let index = k - 1;
  let queue = nums.slice(0, k);
  let result = [];
  while (index < 50000 && queue.length === k) {
    // 保存最大值
    const max = Math.max(...queue);
    result.push(max);
    // 队列一进一出
    index++;
    if (index <= nums.length - 1) queue.push(nums[index]);
    queue.shift();
  }
  return result;
}

